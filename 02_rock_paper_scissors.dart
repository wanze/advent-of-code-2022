enum Shape { rock, paper, scissor }

void main() {
  print('part1: ${part1()}');
  print('part2: ${part2()}');
}

int part1() {
  final List<String> rounds = parseRounds();
  int score = 0;
  for (String round in rounds) {
    final Shape opponentsShape = parseShape(round[0]);
    final Shape myShape = parseShape(round[2]);

    score += calculateScore(opponentsShape, myShape);
  }
  
  return score;
}

int part2() {
  final List<String> rounds = parseRounds();
  int score = 0;
  for (String round in rounds) {
    final Shape opponentsShape = parseShape(round[0]);
    final String result = round[2];

    Shape myShape;
    if (result == 'Y') {
      // draw
      myShape = opponentsShape;
    } else if (result == 'X') {
      // loose
      if (opponentsShape == Shape.paper) {
        myShape = Shape.rock;
      } else if (opponentsShape == Shape.rock) {
        myShape = Shape.scissor;
      } else {
        myShape = Shape.paper;
      }
    } else {
      // win
      if (opponentsShape == Shape.paper) {
        myShape = Shape.scissor;
      } else if (opponentsShape == Shape.rock) {
        myShape = Shape.paper;
      } else {
        myShape = Shape.rock;
      }
    }

    score += calculateScore(opponentsShape, myShape);
  }
  
  return score;
}

int calculateScore(Shape opponent, Shape mine) {
  int score = points(mine);

  if (opponent == mine) {
    score += 3;
  } else if (opponent == Shape.rock) {
    score += mine == Shape.paper ? 6 : 0;
  } else if (opponent == Shape.paper) {
    score += mine == Shape.scissor ? 6 : 0;
  } else if (opponent == Shape.scissor) {
    score += mine == Shape.rock ? 6 : 0;
  }

  return score;  
}

int points(Shape shape) {
  if (shape == Shape.rock) {
    return 1;
  } else if (shape == Shape.paper) {
    return 2;
  }
  return 3;
}

List<String> parseRounds() {
  return input().split('\n');
}

Shape parseShape(String letter) {
  if (letter == 'A' || letter == 'X') {
    return Shape.rock;
  } else if (letter == 'B' || letter == 'Y') {
    return Shape.paper;
  } else if (letter == 'C' || letter == 'Z') {
    return Shape.scissor;
  }

  throw Exception('Unable to parse shape from "$letter"');
}

